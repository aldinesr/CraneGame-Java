package carnegametest222222;

/**
 *
 * @author JustClick
 */
public class BoxArea {
    private int x;
    private int y;
    private int width;
    private int height;

    public BoxArea(int x, int y, int width, int height) {
        this.x = x;
        this.y = y;
        this.width = width;
        this.height = height;
    }

    public int getX() {
        return x;
    }

    public void setX(int x) {
        this.x = x;
    }

    public int getY() {
        return y;
    }

    public void setY(int y) {
        this.y = y;
    }

    public boolean isPointInside(int x, int y) {
        return (x >= this.getX() && x <= this.getX() + this.getWidth()) &&
               (y >= this.getY() && y <= this.getY() + this.getHeight());
    }

    public int getWidth() {
        return width;
    }

    public void setWidth(int width) {
        this.width = width;
    }

    public int getHeight() {
        return height;
    }

    public void setHeight(int height) {
        this.height = height;
    }
}

